    protected JComponent makeTextPanel(String hotelName, String hotelDesc, String hotelLoc, String hotelImg) {
    JPanel panel = new JPanel(false);
    
    
        Font myTextFont = new Font("Tempus Sans ITC", Font.PLAIN,20);
       
        JTextArea hotelNames = new JTextArea("Town: "+hotelName+"\n"
                                            +"\n"+"Town Description: "+hotelDesc+"\n"
                                            +"\n"+"Town History: "+hotelLoc);   
        hotelNames.setFont(myTextFont);
        hotelNames.setWrapStyleWord(true);
        hotelNames.setLineWrap(true);
        hotelNames.setEditable(false);
        hotelNames.setFocusable(false);
        hotelNames.setOpaque(false);
        ImageIcon image = new ImageIcon(getClass().getResource("image/"+hotelImg));
        JLabel townImg = new JLabel(image);
        townImg.setHorizontalAlignment(JLabel.CENTER);
         
        panel.setLayout(new GridLayout(2, 0));
        panel.add(new JScrollPane(townImg,
                        ScrollPaneConstants.VERTICAL_SCROLLBAR_AS_NEEDED,
                        ScrollPaneConstants.HORIZONTAL_SCROLLBAR_NEVER)); 
        panel.add(new JScrollPane(hotelNames,
                        ScrollPaneConstants.VERTICAL_SCROLLBAR_AS_NEEDED,
                        ScrollPaneConstants.HORIZONTAL_SCROLLBAR_NEVER));   
    
    return panel;
  }    
    
    
    private void addTab(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_addTab
        // TODO add your handling code here:
        Towns town = new Towns();
        town.setTowns();
        
        Hotels townHotel = new Hotels();
        townHotel.setHotels();        
        
        int i;
        
        String test = townFrame.townName.getText();             
        
        for(i = 0; i < townHotel.getAllHotelName(test).length; i++){
            if(townHotel.getAllHotelName(test)[i]!=null){
                
                JComponent panel1 = makeTextPanel(townHotel.getAllHotelName(test)[i]
                                    ,townHotel.getAllHotelDesc(test)[i]
                                    ,townHotel.getAllHotelLoc(test)[i]
                                    ,townHotel.getAllHotelImg(test)[i]);               
                
                
                jTabbedPane1.addTab(townHotel.getAllHotelName(test)[i], panel1);               
            }           
        }
    }//GEN-LAST:event_addTab

