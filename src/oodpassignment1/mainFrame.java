    public JComponent makeButtons(String tName, String tDesc, String tLoc, String img){
        JPanel panel = new JPanel(false);
        JButton button = new JButton();
        button.setText("More Info");
        Font myButtonFont = new Font("Tempus Sans ITC", Font.PLAIN,50);
        Font myTextFont = new Font("Tempus Sans ITC", Font.PLAIN,20);
        button.setFont(myButtonFont);
        JTextArea townName = new JTextArea("Town: "+tName+"\n"
                                            +"\n"+"Town Description: "+tDesc+"\n"
                                            +"\n"+"Town History: "+tLoc);   
        townName.setFont(myTextFont);
        townName.setWrapStyleWord(true);
        townName.setLineWrap(true);
        townName.setEditable(false);
        townName.setFocusable(false);
        townName.setOpaque(false);
        ImageIcon image = new ImageIcon(getClass().getResource("image/"+img));
        JLabel townImg = new JLabel(image);
        townImg.setHorizontalAlignment(JLabel.CENTER);
        button.setHorizontalAlignment(JButton.CENTER); 
        panel.setLayout(new GridLayout(3, 1));
        panel.add(new JScrollPane(townImg,
                        ScrollPaneConstants.VERTICAL_SCROLLBAR_AS_NEEDED,
                        ScrollPaneConstants.HORIZONTAL_SCROLLBAR_NEVER)); 
        panel.add(new JScrollPane(townName,
                        ScrollPaneConstants.VERTICAL_SCROLLBAR_AS_NEEDED,
                        ScrollPaneConstants.HORIZONTAL_SCROLLBAR_NEVER));        
        panel.add(button);
        button.addActionListener(new java.awt.event.ActionListener(){
            public void actionPerformed(java.awt.event.ActionEvent evt) {                        
                town = tName;
                closeMainFrame();          
                new townFrame().setVisible(true);              
            }          
        });
        return panel;
    }
    
