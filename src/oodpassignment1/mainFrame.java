    private void addTabs(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_addTabs
        // TODO add your handling code here:
        Towns town = new Towns();
        town.setTowns();
        Town towns = new Town();
        for(int i=0;i<towns.getTowns().length;i++){
            if(towns.getTowns()[i]!=null){
                
                JComponent tab = makeButtons(towns.getTowns()[i],
                                    towns.getTownDesc()[i],
                                    towns.getTownLoc()[i],
                                    towns.getTownImg()[i]);                                        
                jTabbedPane1.addTab(towns.getTowns()[i],tab);  
            }
        }   
        
    }//GEN-LAST:event_addTabs
    
    
    
    
